    <h2>About Us</h2>
    <p>April Kristina started with Rhode Island-native April Petrilli’s love for traveling to Nantucket. With a Pandora bracelet on her wrist, she went in search of a Nantucket charm bead, but came up empty handed. She then realized she wanted to design a sterling silver Nantucket-themed bead to adorn her charm bracelet.</p>
	<p>In stepped her niece Kristina Petrilli, who married April’s nephew Dan in May 2011. Kristina holds a BFA in Studio Art and is a graphic designer and artist. She transformed April's ideas and brought the bead to life!</p>
	<p>Both women have a passion for supporting local Rhode Island businesses so they kept the fabrication of the sterling silver charm bead right here in little Rhody. In October 2010, the April Kristina Nantucket Sterling Silver Charm bead was cast and the first dozen were sold by December in Nantucket!</p>
